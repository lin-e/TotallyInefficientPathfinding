        public static List<int[]> Load(string[] inputGrid)
        {
            List<int[]> newPoints = new List<int[]>();
            for (int y = 0; y < inputGrid.Length; y++)
            {
                for (int x = 0; x < inputGrid[0].Length; x++)
                {
                    if (inputGrid[y][x].ToString() == "1")
                    {
                        newPoints.Add(new int[] { x, y });
                    }
                }
            }
            return newPoints;
        }
